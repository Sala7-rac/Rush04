void    ft_putchar(char c);
void    rush(int x, int y){
    int i = 0;
    int j;

    while (i < y)
    {
        j = 0;
        while (j < x)
        {
            if( (i == 0 && j == 0) || (i == y - 1 && j == x - 1 ) && !(x == 1 || y == 1)){
                ft_putchar('A');
            }
            else if( (i == 0 && j == x - 1) || (i == y - 1 && j == 0 ) )
            {
                ft_putchar('C');
            }
            else if( 
                ( i == 0 && (j >= 1 && j <= x - 2)) ||
                ( i == y - 1 && (j >= 1 && j <= x - 2))||
                ( j == 0     && (i >= 1 && i <= y - 2))||
                ( j == x - 1 && (i >= 1 && i <= y - 2))
            )
            //(i == 0 || i == y - 1) || (j == 0 || j == x - 1)) 
            {
                ft_putchar('B');
            }else{
                ft_putchar(' ');
            }
            j++;
        }
        ft_putchar('\n');
        i++;
    }
}